package ASSIGNMENT;

public class CIRCLE {
	public static void main(String[] args) {
		double radius=5;
		
		double area=Math.PI*radius*radius;
		double perimeter=2*Math.PI*radius;
		
		System.out.println("Area of Circle is:"+area);
		System.out.println("Perimeter of circle is:"+perimeter);
}
}